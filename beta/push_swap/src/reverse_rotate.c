/* ************************************************************************** */
/*                                                                            */
/*   Функция `reverse_rotate_rra` выполняет обратный ротационный              */
/*   сдвиг стека `a`.                                                         */
/*   Последний элемент стека становится первым, а все остальные сдвигаются на */
/*   одну позицию вниз. Операция используется в алгоритмах сортировки.        */
/*                                                                            */
/*   Параметры:                                                               */
/*   - `stack_a`: двойной указатель на начало стека `a`.                      */
/*   - `flag`: флаг для определения необходимости вывода операции.            */
/*   - `fd`: файловый дескриптор для вывода операции.                         */
/*                                                                            */
/*   Работа функции:                                                          */
/*   1. Проверка, что стек существует и содержит более одного элемента.       */
/*   2. Поиск предпоследнего элемента стека.                                  */
/*   3. Перемещение последнего элемента в начало стека.                       */
/*   4. Если установлен `flag`, вывод названия операции через файловый        */
/*      дескриптор `fd`.                                                      */
/*                                                                            */
/*   Возвращаемое значение:                                                   */
/*   - Функция не возвращает значение.                                        */
/*                                                                            */
/* ************************************************************************** */

void	reverse_rotate_rra(t_stack **stack_a, int flag, int fd)
{
	t_stack	*tmp;
	t_stack	*tmp2;

	if (stack_a && *stack_a && (*stack_a)->next)
	{
		tmp = *stack_a;
		while (tmp->next->next)
			tmp = tmp->next;
		tmp2 = tmp->next;
		tmp->next = NULL;
		tmp2->next = *stack_a;
		*stack_a = tmp2;
		if (flag)
			write(fd, "rra\n", 4);
	}
}

/* ************************************************************************** */
/*                                                                            */
/*   Функция `reverse_rotate_rrb` выполняет обратный ротационный сдвиг        */
/*   стека `b`.                                                               */
/*   Аналогично `reverse_rotate_rra`, перемещает последний элемент в начало   */
/*   стека `b`.                                                               */
/*                                                                            */
/*   Параметры:                                                               */
/*   - `stack_b`: двойной указатель на начало стека `b`.                      */
/*   - `flag`: флаг для определения необходимости вывода операции.            */
/*   - `fd`: файловый дескриптор для вывода операции.                         */
