/* ************************************************************************** */
/*                                                                            */
/*   Функция `reverse_rotate_rrr` выполняет обратный ротационный сдвиг обоих  */
/*   стеков `a` и `b` одновременно. Это комбинированная операция, состоящая   */
/*   из `reverse_rotate_rra` и `reverse_rotate_rrb`.                          */
/*                                                                            */
/*   Параметры:                                                               */
/*   - `stack_a`: двойной указатель на начало стека `a`.                      */
/*   - `stack_b`: двойной указатель на начало стека `b`.                      */
/*   - `flag`: флаг для определения необходимости вывода операции.            */
/*   - `fd`: файловый дескриптор для вывода комбинированной операции.         */
/*                                                                            */
/*   Работа функции:                                                          */
/*   1. Проверка, что оба стека существуют и содержат более одного элемента.  */
/*   2. Вызов `reverse_rotate_rra` и `reverse_rotate_rrb` для каждого стека.  */
/*   3. Вывод названия комбинированной операции "rrr" через файловый          */
/*      дескриптор `fd`.                                                      */
/*                                                                            */
/*   Возвращаемое значение:                                                   */
/*   - Функция не возвращает значение.                                        */
/*                                                                            */
/* ************************************************************************** */

void	reverse_rotate_rrr(t_stack **stack_a, t_stack **stack_b, int flag,
			int fd)
{
	if (stack_a && *stack_a && (*stack_a)->next
		&& stack_b && *stack_b && (*stack_b)->next)
	{
		reverse_rotate_rra(stack_a, 0, 0);
		reverse_rotate_rrb(stack_b, 0, 0);
		if (flag)
			write(fd, "rrr\n", 4);
	}
}
