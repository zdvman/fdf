#include "libft.h"

int	ft_iszero(char *arg)
{
	int	i;

	i = 0;
	if (ft_issign(arg[i]))
		i++;
	while (arg[i] && arg[i] == '0')
		i++;
	if (arg[i] != '\0')
		return (0);
	return (1);
}
